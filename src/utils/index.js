// 首字母大小
export function titleCase(str) {
  return str.replace(/( |^)[a-z]/g, L => L.toUpperCase());
}

// 下划转驼峰
export function camelCase(str) {
  return str.replace(/-[a-z]/g, str1 => str1.substr(-1).toUpperCase());
}

export function isNumberStr(str) {
  return /^[+-]?(0|([1-9]\d*))(\.\d+)?$/g.test(str);
}

// 限制输入数字,小数点位数
export function limitInput(value, precision = 2) {
  let str = (value && value.split("")) || [];
  let reg1 = /\d/;
  let reg2 = /\./;
  let reg3 = /\-/;
  //如果第一位是0，第二位不是点，就用数字把点替换掉
  if (str.length > 1 && str[0] == 0 && str[1] != ".") {
    str.splice(0, 1);
  }
  // 第一个字符不能为小数点
  if (str[0] == ".") {
    return "";
  }
  // 过滤掉除数字和小数点外的字符
  const val = str.filter(e => reg1.test(e) || reg2.test(e) || reg3.test(e));
  // 匹配小数点后只能有两位小数
  // 解释一下这个match正则规格
  // ^\d* 是指以数字开头，后面允许输入0到多位数字
  // (\.?) 是指只允许一个小数点
  // \d{0,2} 是指只允许0到2位小数
  if (precision === 1)
    return val.join("").match(/^[-|\d]*(\.?\d{0,1})/g)[0] || null;
  return val.join("").match(/^[-|\d]*(\.?\d{0,2})/g)[0] || null;
}

export function accAdd(arg1, arg2) {
  var r1, r2, m;
  try {
    r1 = arg1.toString().split(".")[1].length;
  } catch (e) {
    r1 = 0;
  }
  try {
    r2 = arg2.toString().split(".")[1].length;
  } catch (e) {
    r2 = 0;
  }
  m = Math.pow(10, Math.max(r1, r2));
  return (arg1 * m + arg2 * m) / m;
}

//减法函数，用来得到精确的减法结果
export function accSub(arg1, arg2) {
  var r1, r2, m, n;
  try {
    r1 = arg1.toString().split(".")[1].length;
  } catch (e) {
    r1 = 0;
  }
  try {
    r2 = arg2.toString().split(".")[1].length;
  } catch (e) {
    r2 = 0;
  }
  m = Math.pow(10, Math.max(r1, r2));
  //last modify by deeka
  //动态控制精度长度
  n = r1 >= r2 ? r1 : r2;
  return ((arg1 * m - arg2 * m) / m).toFixed(n);
}

//除法函数，用来得到精确的除法结果
//说明：javascript的除法结果会有误差，在两个浮点数相除的时候会比较明显。这个函数返回较为精确的除法结果。
//调用：accDiv(arg1,arg2)
//返回值：arg1除以arg2的精确结果
export function accDiv(arg1, arg2, digit) {
  var t1 = 0,
    t2 = 0,
    r1,
    r2;
  try {
    t1 = arg1.toString().split(".")[1].length;
  } catch (e) {}
  try {
    t2 = arg2.toString().split(".")[1].length;
  } catch (e) {}
  r1 = Number(arg1.toString().replace(".", ""));
  r2 = Number(arg2.toString().replace(".", ""));
  //获取小数点后的计算值
  var result = ((r1 / r2) * Math.pow(10, t2 - t1)).toString();
  var result2 = result.split(".")[1] ? result.split(".")[1] : 0;

  if (result2) {
    result2 = result2.substring(
      0,
      digit > result2.length ? result2.length : digit
    );
    return Number(result.split(".")[0] + "." + result2);
  } else {
    return Number(result.split(".")[0]);
  }
}

//乘法函数，用来得到精确的乘法结果
//说明：javascript的乘法结果会有误差，在两个浮点数相乘的时候会比较明显。这个函数返回较为精确的乘法结果。
//调用：accMul(arg1,arg2)
//返回值：arg1乘以arg2的精确结果
export function accMul(arg1, arg2) {
  var m = 0;
  var s1 = arg1.toString();
  var s2 = arg2.toString();
  try {
    m += s1.split(".")[1].length;
  } catch (e) {}
  try {
    m += s2.split(".")[1].length;
  } catch (e) {}

  return (
    (Number(s1.replace(".", "")) * Number(s2.replace(".", ""))) /
    Math.pow(10, m)
  );
}

export function rafThrottle(fn) {
  let locked = false;
  return function(...args) {
    if (locked) return;
    locked = true;
    window.requestAnimationFrame(_ => {
      fn.apply(this, args);
      locked = false;
    });
  };
}

